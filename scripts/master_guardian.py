import os
import requests
from github import Github
from datetime import datetime

def main():
    print("=== Master AI Guardian Started ===")

    api_key = os.environ.get("CLAUDE_API_KEY")
    github_token = os.environ.get("AGENT_GITHUB_TOKEN")

    if not api_key:
        print("ERROR: No API key found!")
        return

    user_command = os.environ.get("GUARDIAN_COMMAND", "")
    user_command_lower = user_command.lower()

    # Read context and feedback
    context = ""
    try:
        with open("context.md", "r", encoding="utf-8") as f:
            context = f.read()
    except:
        pass

    feedback = ""
    try:
        with open("feedback.md", "r", encoding="utf-8") as f:
            feedback = f.read()
    except:
        pass

    # === 1. Create GitHub Issue ===
    create_issue_keywords = ["create an issue", "create issue", "make an issue", "open an issue"]
    if any(kw in user_command_lower for kw in create_issue_keywords):
        print("Creating GitHub Issue...")
        try:
            g = Github(github_token)
            repo = g.get_repo("AllahRakhha/SavingsClub")

            issue_title = f"AI Guardian Suggestions - {datetime.now().strftime('%Y-%m-%d')}"
            issue_body = f"**Generated by Master AI Guardian**\n\n{user_command}"

            issue = repo.create_issue(title=issue_title, body=issue_body)
            print(f"Issue created successfully: {issue.html_url}")
        except Exception as e:
            print(f"Failed to create Issue: {e}")
        return

    # === 2. Create Pull Request from existing Issue ===
    if "create pr for issue" in user_command_lower:
        print("Creating Pull Request from Issue...")
        try:
            # Extract issue number from command (e.g. "Create PR for issue #45")
            import re
            match = re.search(r"#(\d+)", user_command)
            if not match:
                print("Please specify issue number like: Create PR for issue #45")
                return

            issue_number = int(match.group(1))

            g = Github(github_token)
            repo = g.get_repo("AllahRakhha/SavingsClub")
            issue = repo.get_issue(issue_number)

            # Create branch
            base = repo.get_branch("main")
            branch_name = f"ai-suggestions-{datetime.now().strftime('%Y%m%d%H%M')}"
            repo.create_git_ref(ref=f"refs/heads/{branch_name}", sha=base.commit.sha)

            # Create Pull Request
            pr_title = f"AI Guardian Suggestions from Issue #{issue_number}"
            pr_body = f"**This PR contains suggestions from Issue #{issue_number}**\n\n{issue.body}"

            pr = repo.create_pull(title=pr_title, body=pr_body, head=branch_name, base="main")
            print(f"Pull Request created: {pr.html_url}")

        except Exception as e:
            print(f"Failed to create Pull Request: {e}")
        return

    # === Normal Audit (if no special command) ===
    print("Running normal audit... (Add your full audit logic here later)")

if __name__ == "__main__":
    main()
